class StoreGood {
  constructor(id, name, stock, price, condition, listed, createdTime, product) {
    this.id = id;
    this.name = name;
    this.stock = stock;
    this.price = price;
    this.condition = condition;
    this.listed = listed;
    this.createdTime = createdTime;
    this.product = product;
  }
}

export default StoreGood;
